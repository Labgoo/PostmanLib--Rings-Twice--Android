public class SampleList extends FragmentActivity implements AdapterView.OnItemClickListener {
    private ListView mSamples;
    private String[] sampleList = { "Twitter", "NoAuth", "Linkedin"  };

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);    // Autogenerated
        setContentView(R.layout.sample_list);

        mSamples = (ListView) findViewById(R.id.SampleList);

        final ArrayAdapter<String> adp = new ArrayAdapter<String>(this, android.R.layout.simple_list_item_1, sampleList);
        mSamples.setAdapter(adp);
        mSamples.setOnItemClickListener(this);
    }

    @Override
    public void onItemClick(AdapterView<?> adapterView, View view, int i, long l) {
        Intent j;
        switch((int) l){
            case 0:
                j = new Intent(this, TwitterSample.class);
            break;
            default:
                return;
        }

        startActivity(j);
    }
}
